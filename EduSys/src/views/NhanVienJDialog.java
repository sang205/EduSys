    void init() {
        setLocationRelativeTo(null);

        this.fillToTable();
        this.row = -1;
        this.setStatus(true);
    }

    void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        try {
            List<NhanVien> list = dao.select();
            for (NhanVien nv : list) {
                Object[] rows = {nv.getMaNv(), nv.getMatKhau(), nv.getHoTen(), nv.getVaiTro() ? "Trưởng phòng" : "Nhân viên"};
                model.addRow(rows);
            }
        } catch (Exception e) {
            DialogOption.alert(this, "Error!!!");
        }
    }

    private NhanVien getForm() {
        NhanVien nv = new NhanVien();
        nv.setMaNv(txtMaNV.getText());
        nv.setHoTen(txtName.getText());
        nv.setMatKhau(new String(txtPassword.getPassword()));
        nv.setVaiTro(rdoTruongPhong.isSelected());
        return nv;
    }

    void setForm(NhanVien nv) {
        txtMaNV.setText(nv.getMaNv());
        txtName.setText(nv.getHoTen());
        txtPassword.setText(nv.getMatKhau());
        txtConfirmPassword.setText(nv.getMatKhau());

        Boolean vaiTro = nv.getVaiTro();
        if (vaiTro != null) {
            rdoTruongPhong.setSelected(vaiTro);
            rdoNhanVien.setSelected(!vaiTro);
        } else {
            rdoTruongPhong.setSelected(false);
            rdoNhanVien.setSelected(true);
        }
    }

    void clearForm() {
        this.setForm(new NhanVien());
        this.row = -1;
        this.setStatus(true);
    }

    void update() {
        NhanVien form = getForm();

        String confirm = new String(txtConfirmPassword.getPassword());
        if (!confirm.equals(form.getMatKhau())) {
            DialogOption.alert(this, "Xác nhận mật khẩu không đúng!");
        } else {
            try {
                dao.update(form);
                this.fillToTable();
                DialogOption.alert(this, "Cập nhật thành công!");
            } catch (Exception e) {
                DialogOption.alert(this, "Cập nhật thất bại!");
            }
        }
    }

    void edit() {
        try {
            NhanVien nv = dao.findByID((String) tblNhanVien.getValueAt(this.row, 0));
            if (nv != null) {
                this.setForm(nv);
                tabs.setSelectedIndex(0);
                this.setStatus(false);
            }
        } catch (Exception e) {
            DialogOption.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    void insert() {
        NhanVien form = getForm();
        String confirm = new String(txtConfirmPassword.getPassword());
        if (confirm.equals(form.getMatKhau())) {
            try {
                dao.insert(form);
                this.fillToTable();
                this.clearForm();
                DialogOption.alert(this, "Thêm mới thành công!");
            } catch (Exception e) {
                DialogOption.alert(this, "Thêm mới thất bại!");
            }
        } else {
            DialogOption.alert(this, "Xác nhận mật khẩu không đúng!");
        }
    }

    void delete() {
        if (!Auth.isManager()) {
            DialogOption.alert(this, "Bạn không có quyền xóa nhân viên.");
        } else {
            String manv = txtMaNV.getText();
            if (manv.equals(Auth.user.getMaNv())) {
                DialogOption.alert(this, "Bạn không thể tự xóa chính mình!");
            } else if (DialogOption.confirm(this, "Bạn có thật sự muốn xóa nhân viên này?")) {
                try {
                    dao.delete(manv);
                    this.fillToTable();
                    this.clearForm();
                    DialogOption.alert(this, "Xóa thành công");
                } catch (Exception e) {
                    DialogOption.alert(this, "Xóa thất bại!");
                }
            }
        }
    }

    void setStatus(boolean insertable) {
        txtMaNV.setEditable(insertable);
        btnAdd.setEnabled(insertable);
        btnUpdate.setEnabled(!insertable);
        btnDelete.setEnabled(!insertable);

        boolean first = this.row > 0;
        boolean last = this.row < tblNhanVien.getRowCount() - 1;
        btnFirst.setEnabled(!insertable && first);
        btnPrevious.setEnabled(!insertable && first);
        btnNext.setEnabled(!insertable && last);
        btnLast.setEnabled(!insertable && last);
    }

